#include "fillit.h"

int	ft_x_left(t_list *pos)
{
	int i;

	i = 0;
	while (i < 4)
	{
		if ((pos->x[i] - 1) < 0)
			return (0);
		i++;
	}
	i = 0;
	while (i < 4)
	{
		pos->x[i]--;
		i++;
	}
	return (1);
}
